-- Задание 1.1
-- По каждому преподавателю вывести его имя и сумму hiredate + salary (столбец "Дата").
-- Интерпретация: прибавляем к дате приема на работу количество дней, равное ставке (Salary).
SELECT TeacherID, Name,
       DATEADD(day, Salary, Hiredate) AS "Дата"
FROM TEACHER;
