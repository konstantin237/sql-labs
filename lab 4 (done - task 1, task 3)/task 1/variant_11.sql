-- Задание 1.13 (вариант 11)
-- "Университет <Название> расположен в городе <Город> имеет рейтинг <Рейтинг>"
-- Вариант 1: один столбец
SELECT UnivID,
       CONCAT('Университет ', COALESCE(UnivName,''), ' расположен в городе ', COALESCE(City,''), ' имеет рейтинг ', COALESCE(CAST(Reiting AS varchar(10)),'0')) AS "Сведения об университетах"
FROM UNIVERS;

-- Вариант 2: шесть столбцов
SELECT UnivID,
       'Константа1' AS "Константа1",
       UnivName    AS "Название_университета",
       'Константа2' AS "Константа2",
       City         AS "Город",
       'Константа3' AS "Константа3",
       Reiting      AS "Рейтинг"
FROM UNIVERS;
