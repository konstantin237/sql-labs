-- Задание 1.8 (вариант 6)
-- Информация о дисциплинах: Название, uppercase, lowercase
-- Вариант 1: один столбец
SELECT SubjID,
       CONCAT('Название дисциплины в БД: ', Name, ' Название дисциплины прописными буквами: ', UPPER(Name),
              ' Название дисциплины строчными буквами: ', LOWER(Name)) AS "Информация о дисциплинах"
FROM SUBJECT;

-- Вариант 2: шесть столбцов
SELECT SubjID,
       'Константа1' AS "Константа1",
       Name         AS "Дисциплина1",
       'Константа2' AS "Константа2",
       UPPER(Name)  AS "Дисциплина2",
       'Константа3' AS "Константа3",
       LOWER(Name)  AS "Дисциплина3"
FROM SUBJECT;
