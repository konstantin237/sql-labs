-- Задание 1.6 (вариант 4)
-- Информация об аудиториях: "Аудитория <номер аудитории> находится на <номер этажа> этаже корпуса <номер корпуса> и имеет <количество мест> мест"
-- Вариант 1: всё в одном столбце
SELECT RoomID,
       CONCAT('Аудитория ', CAST(Num AS varchar(20)), ' находится на ', COALESCE(CAST(Floor AS varchar(10)),'?'),
              ' этаже корпуса ', CAST(Building AS varchar(10)), ' и имеет ', COALESCE(CAST(Seats AS varchar(10)),'0'), ' мест')
       AS "Информация об аудиториях"
FROM ROOM;

-- Вариант 2: в девяти столбцах
SELECT RoomID,
       'Литерал1' AS "Литерал1",
       Num        AS "Аудитория",
       'Литерал2' AS "Литерал2",
       Floor      AS "Этаж",
       'Литерал3' AS "Литерал3",
       Building   AS "Корпус",
       'Литерал4' AS "Литерал4",
       Seats      AS "К-во мест",
       'Литерал5' AS "Литерал5"
FROM ROOM;
